public class Group {
        private String author1, author2;
        private String feat1, feat2, feat3;
        private String category, title;

        public Group() {
        }

        public String getAuthor1() {
            return author1;
        }

        public void setAuthor1(String author1) {
            this.author1 = author1;
        }

        public String getAuthor2() {
            return author2;
        }

        public void setAuthor2(String author2) {
            this.author2 = author2;
        }

        public String getFeat1() {
            return feat1;
        }

        public void setFeat1(String feat1) {
            this.feat1 = feat1;
        }

        public String getFeat2() {
            return feat2;
        }

        public void setFeat2(String feat2) {
            this.feat2 = feat2;
        }

        public String getFeat3() {
            return feat3;
        }

        public void setFeat3(String feat3) {
            this.feat3 = feat3;
        }

        public String getCategory() {
            return category;
        }

        public void setCategory(String category) {
            this.category = category;
        }

        public String getTitle() {
            return title;
        }

        public void setTitle(String title) {
            this.title = title;
        }
}

